#!/usr/bin/env node
// Generates deterministic synthetic TypeScript corpora for benchmarking.
//
// Output:
//   bench/corpus/small  (~5k LOC, single-file-style modules)
//   bench/corpus/medium (~50k LOC, single-file-style modules)
//   bench/corpus/multi  (~100k LOC across 200 files in 5 layers with a
//     realistic cross-import graph â€” every layer-k file imports several
//     layer-(k-1) files, an entry.ts reaches everything transitively; M5
//     end-to-end benchmark: `ztsc corpus/multi/entry.ts`)
//   bench/corpus/skewed (~70k LOC: one very large file next to many small
//     ones in import chains of mixed depth â€” the discovery-skew stress
//     from ROADMAP "single-owner module discovery": under wavefront
//     scheduling the huge file's wave is a barrier and the deep chains
//     degenerate toward serial discovery)
//   bench/corpus/bigfan (~67k LOC: several large files among many small ones,
//     with the large files interleaved in entry.ts so their BFS ids share a
//     residue mod 4 â€” the check-partition stress from ROADMAP Â§5, M10:
//     round-robin (id % N) clumps the large files onto one checker while the
//     greedy cost-based partition spreads them one per checker)
// The generated code stays inside the ztsc v0.0.1 subset (PLAN.md Â§5):
// interfaces, type aliases, unions, classes, functions, narrowing.
//
// Usage: node bench/gen_corpus.js [--force]

"use strict";

const fs = require("fs");
const path = require("path");

// One generated module is ~110 lines (lib-free: the ztsc checker has no
// lib.d.ts, so the corpus avoids globals and primitive/array methods).
const SIZES = {
  small: { files: 10, modulesPerFile: 5 }, // ~5k LOC
  medium: { files: 50, modulesPerFile: 9 }, // ~50k LOC
};

// Deterministic PRNG (LCG) so corpora are reproducible.
function makeRng(seed) {
  let s = seed >>> 0;
  return () => {
    s = (s * 1664525 + 1013904223) >>> 0;
    return s / 0x100000000;
  };
}

function genModule(fileIdx, modIdx, rng) {
  const id = `F${fileIdx}M${modIdx}`;
  const kinds = ['"circle"', '"square"'];
  const kind = kinds[Math.floor(rng() * kinds.length)];
  const n = Math.floor(rng() * 100);
  return `// module ${id}
export interface Point${id} {
  x: number;
  y: number;
  label?: string;
}

export interface Circle${id} {
  kind: "circle";
  center: Point${id};
  radius: number;
}

export interface Square${id} {
  kind: "square";
  origin: Point${id};
  side: number;
}

export type Shape${id} = Circle${id} | Square${id};

export type Result${id} = { ok: true; value: number } | { ok: false; error: string };

export function area${id}(shape: Shape${id}): number {
  if (shape.kind === "circle") {
    return 3.14159 * shape.radius * shape.radius;
  }
  return shape.side * shape.side;
}

export function describe${id}(shape: Shape${id}, verbose?: boolean): string {
  const a = area${id}(shape);
  if (verbose && a > ${n}) {
    return shape.kind + "!";
  }
  return shape.kind;
}

export function tryDivide${id}(a: number, b: number): Result${id} {
  if (b === 0) {
    return { ok: false, error: "division by zero" };
  }
  return { ok: true, value: a / b };
}

export function unwrap${id}(r: Result${id}, fallback: number): number {
  if (r.ok) {
    return r.value;
  }
  return r.error.length === 0 ? fallback : 0 - fallback;
}

export class Registry${id}<T> {
  items: T[] = [];
  size: number = 0;

  add(item: T): number {
    this.items[this.size] = item;
    this.size = this.size + 1;
    return this.size;
  }

  get(index: number): T | undefined {
    return index < this.size ? this.items[index] : undefined;
  }

  count(): number {
    return this.size;
  }
}

export function process${id}(input: string | number | null): string {
  if (input === null) {
    return "null";
  }
  if (typeof input === "number") {
    return "n:" + input;
  }
  return input;
}

export function firstLabel${id}(points: Point${id}[]): string {
  for (const p of points) {
    if (p.label) {
      return p.label;
    }
  }
  return "none";
}

export function fmt${id}(x: string): string;
export function fmt${id}(x: number): string;
export function fmt${id}(x: string | number): string {
  if (typeof x === "string") {
    return x;
  }
  return "#" + x;
}

export function ident${id}<T>(x: T): T {
  return x;
}

const registry${id} = new Registry${id}<Shape${id}>();
registry${id}.add({ kind: ${kind}, ${
    kind === '"square"' ? "origin: { x: 0, y: 0 }, side: 2" : "center: { x: 0, y: 0 }, radius: 1"
  } });
export const count${id}: number = registry${id}.count();
export const desc${id}: string = describe${id}({ kind: "circle", center: { x: 1, y: 1 }, radius: 2 }, true);
export const num${id}: number = ident${id}(${n});
export const label${id}: string = fmt${id}(count${id});
`;
}

function genFile(fileIdx, modulesPerFile, rng) {
  let out = `// Generated by bench/gen_corpus.js â€” do not edit.\n\n`;
  for (let m = 0; m < modulesPerFile; m++) {
    out += genModule(fileIdx, m, rng) + "\n";
  }
  return out;
}

// --- multi-file corpus (M5) -------------------------------------------------

const MULTI = {
  layers: 5,
  filesPerLayer: 40, // 200 files
  modulesPerFile: 4, // ~110 lines each -> ~450 local lines/file
  depsPerFile: 3,
};

function multiFileId(layer, idx) {
  return `L${layer}F${idx}`;
}

function genMultiFile(layer, idx, deps, rng) {
  const fid = multiFileId(layer, idx);
  let out = `// Generated by bench/gen_corpus.js (multi corpus) â€” do not edit.\n\n`;
  // Imports from the previous layer (value + type + type-only).
  for (const d of deps) {
    const did = `F${multiFileId(layer - 1, d)}M0`;
    out += `import { area${did}, describe${did}, count${did}, Shape${did} } from "../l${layer - 1}/f_${String(d).padStart(3, "0")}";\n`;
    out += `import type { Point${did} } from "../l${layer - 1}/f_${String(d).padStart(3, "0")}";\n`;
  }
  out += "\n";
  // Local modules (same shape as the single-file corpus).
  for (let m = 0; m < MULTI.modulesPerFile; m++) {
    out += genModule(fid, m, rng) + "\n";
  }
  // Cross-file usage: type-correct consumption of every dependency.
  for (const [j, d] of deps.entries()) {
    const did = `F${multiFileId(layer - 1, d)}M0`;
    out += `const shape${fid}_${j}: Shape${did} = { kind: "circle", center: { x: ${j}, y: 1 }, radius: 2 };\n`;
    out += `export const via${fid}_${j}: number = area${did}(shape${fid}_${j}) + count${did};\n`;
    out += `export function relabel${fid}_${j}(p: Point${did}, v: boolean): string {\n`;
    out += `  if (p.label && v) {\n    return p.label;\n  }\n`;
    out += `  return describe${did}(shape${fid}_${j}, v);\n}\n`;
  }
  return out;
}

function genMulti(root, force) {
  const dir = path.join(root, "multi");
  if (fs.existsSync(dir) && !force) {
    console.log(`corpus 'multi' already exists at ${dir} (use --force to regenerate)`);
    return 0;
  }
  fs.mkdirSync(dir, { recursive: true });
  const rng = makeRng(0xbadcafe);
  let loc = 0;
  for (let layer = 0; layer < MULTI.layers; layer++) {
    const ldir = path.join(dir, `l${layer}`);
    fs.mkdirSync(ldir, { recursive: true });
    for (let f = 0; f < MULTI.filesPerLayer; f++) {
      // Deterministic deps: cover every previous-layer file (round robin)
      // plus rng extras, so entry.ts reaches all files transitively.
      const deps = [];
      if (layer > 0) {
        deps.push(f % MULTI.filesPerLayer);
        while (deps.length < MULTI.depsPerFile) {
          const d = Math.floor(rng() * MULTI.filesPerLayer);
          if (!deps.includes(d)) deps.push(d);
        }
      }
      const content = layer === 0
        ? `// Generated by bench/gen_corpus.js (multi corpus) â€” do not edit.\n\n` +
          Array.from({ length: MULTI.modulesPerFile }, (_, m) => genModule(multiFileId(0, f), m, rng)).join("\n")
        : genMultiFile(layer, f, deps, rng);
      fs.writeFileSync(path.join(ldir, `f_${String(f).padStart(3, "0")}.ts`), content);
      loc += content.split("\n").length;
    }
  }
  // entry.ts pulls in the whole top layer.
  let entry = `// Generated by bench/gen_corpus.js (multi corpus) â€” do not edit.\n\n`;
  const top = MULTI.layers - 1;
  for (let f = 0; f < MULTI.filesPerLayer; f++) {
    entry += `import { via${multiFileId(top, f)}_0 } from "./l${top}/f_${String(f).padStart(3, "0")}";\n`;
  }
  entry += `\nexport const total: number = ${Array.from(
    { length: MULTI.filesPerLayer },
    (_, f) => `via${multiFileId(top, f)}_0`,
  ).join(" + ")};\n`;
  fs.writeFileSync(path.join(dir, "entry.ts"), entry);
  loc += entry.split("\n").length;
  fs.writeFileSync(
    path.join(dir, "tsconfig.json"),
    JSON.stringify(
      {
        compilerOptions: {
          strict: true,
          noEmit: true,
          target: "esnext",
          module: "esnext",
          moduleResolution: "bundler",
          lib: ["esnext"],
          types: [],
        },
        files: ["entry.ts"],
      },
      null,
      2,
    ) + "\n",
  );
  const nFiles = MULTI.layers * MULTI.filesPerLayer + 1;
  console.log(`generated corpus 'multi': ${nFiles} files, ~${loc} LOC in ${dir}`);
  return loc;
}

// --- skewed corpus (ROADMAP: single-owner discovery) --------------------------

const SKEWED = {
  hugeModules: 200, // one ~22k-line file
  chains: 8, // independent chains of small files...
  minDepth: 12, // ...of mixed import depth
  maxDepth: 40,
  modulesPerSmallFile: 2, // ~230 lines per small file
};

function skewedFileId(chain, depth) {
  return `C${chain}D${depth}`;
}

function skewedChainDepth(chain) {
  // Mixed depths, deterministic: spread linearly between min and max.
  const { chains, minDepth, maxDepth } = SKEWED;
  return minDepth + Math.round((chain * (maxDepth - minDepth)) / (chains - 1));
}

function genSkewedChainFile(chain, depth, rng) {
  const fid = skewedFileId(chain, depth);
  let out = `// Generated by bench/gen_corpus.js (skewed corpus) â€” do not edit.\n\n`;
  const last = depth + 1 === skewedChainDepth(chain);
  if (!last) {
    const did = `F${skewedFileId(chain, depth + 1)}M0`;
    out += `import { area${did}, count${did}, Shape${did} } from "./d${String(depth + 1).padStart(3, "0")}";\n\n`;
    for (let m = 0; m < SKEWED.modulesPerSmallFile; m++) {
      out += genModule(fid, m, rng) + "\n";
    }
    out += `const shape${fid}: Shape${did} = { kind: "circle", center: { x: 0, y: 1 }, radius: 2 };\n`;
    out += `export const via${fid}: number = area${did}(shape${fid}) + count${did};\n`;
  } else {
    for (let m = 0; m < SKEWED.modulesPerSmallFile; m++) {
      out += genModule(fid, m, rng) + "\n";
    }
    out += `export const via${fid}: number = countF${fid}M0;\n`;
  }
  return out;
}

function genSkewed(root, force) {
  const dir = path.join(root, "skewed");
  if (fs.existsSync(dir) && !force) {
    console.log(`corpus 'skewed' already exists at ${dir} (use --force to regenerate)`);
    return 0;
  }
  fs.mkdirSync(dir, { recursive: true });
  const rng = makeRng(0x5eed);
  let loc = 0;
  let nFiles = 1; // entry.ts
  // The one huge file (~SKEWED.hugeModules * 112 lines).
  let huge = `// Generated by bench/gen_corpus.js (skewed corpus) â€” do not edit.\n\n`;
  for (let m = 0; m < SKEWED.hugeModules; m++) {
    huge += genModule("HUGE", m, rng) + "\n";
  }
  fs.writeFileSync(path.join(dir, "huge.ts"), huge);
  loc += huge.split("\n").length;
  nFiles += 1;
  // The chains of small files.
  for (let c = 0; c < SKEWED.chains; c++) {
    const cdir = path.join(dir, `c${c}`);
    fs.mkdirSync(cdir, { recursive: true });
    for (let d = 0; d < skewedChainDepth(c); d++) {
      const content = genSkewedChainFile(c, d, rng);
      fs.writeFileSync(path.join(cdir, `d${String(d).padStart(3, "0")}.ts`), content);
      loc += content.split("\n").length;
      nFiles += 1;
    }
  }
  // entry.ts pulls in the huge file and every chain head.
  let entry = `// Generated by bench/gen_corpus.js (skewed corpus) â€” do not edit.\n\n`;
  entry += `import { areaFHUGEM0, countFHUGEM0 } from "./huge";\n`;
  for (let c = 0; c < SKEWED.chains; c++) {
    entry += `import { via${skewedFileId(c, 0)} } from "./c${c}/d000";\n`;
  }
  entry += `\nexport const total: number = areaFHUGEM0({ kind: "square", origin: { x: 0, y: 0 }, side: 3 }) + countFHUGEM0 + ${Array.from(
    { length: SKEWED.chains },
    (_, c) => `via${skewedFileId(c, 0)}`,
  ).join(" + ")};\n`;
  fs.writeFileSync(path.join(dir, "entry.ts"), entry);
  loc += entry.split("\n").length;
  fs.writeFileSync(
    path.join(dir, "tsconfig.json"),
    JSON.stringify(
      {
        compilerOptions: {
          strict: true,
          noEmit: true,
          target: "esnext",
          module: "esnext",
          moduleResolution: "bundler",
          lib: ["esnext"],
          types: [],
        },
        files: ["entry.ts"],
      },
      null,
      2,
    ) + "\n",
  );
  console.log(`generated corpus 'skewed': ${nFiles} files, ~${loc} LOC in ${dir}`);
  return loc;
}

// --- bigfan corpus (ROADMAP Â§5, M10: cost-based check partition) --------------
//
// Several large files among many small ones, arranged so the round-robin
// (id % N) partition clumps large files onto one checker while the greedy
// cost-based partition spreads them one per checker. entry.ts imports every
// file; the import order *is* the BFS id-assignment order, so we drop a big
// file every `stride` specifiers to land the bigs on ids that share a residue
// mod 4 (the default --checkers). All files are leaves (no cross-imports).
const BIGFAN = {
  bigFiles: 4, // ~5.8k lines each
  bigModules: 52,
  smallFiles: 200, // ~230 lines each
  modulesPerSmallFile: 2,
  stride: 4, // one big every `stride` entry specifiers -> ids 1,5,9,13 (â‰ˇ1 mod 4)
};

function genBigfan(root, force) {
  const dir = path.join(root, "bigfan");
  if (fs.existsSync(dir) && !force) {
    console.log(`corpus 'bigfan' already exists at ${dir} (use --force to regenerate)`);
    return 0;
  }
  fs.mkdirSync(dir, { recursive: true });
  const rng = makeRng(0xb16fa2);
  let loc = 0;
  let nFiles = 1; // entry.ts

  // The large files (leaves: just many local modules, no imports).
  for (let b = 0; b < BIGFAN.bigFiles; b++) {
    let big = `// Generated by bench/gen_corpus.js (bigfan corpus) â€” do not edit.\n\n`;
    for (let m = 0; m < BIGFAN.bigModules; m++) {
      big += genModule(`BIG${b}`, m, rng) + "\n";
    }
    fs.writeFileSync(path.join(dir, `big_${b}.ts`), big);
    loc += big.split("\n").length;
    nFiles += 1;
  }

  // The many small files (also leaves).
  for (let s = 0; s < BIGFAN.smallFiles; s++) {
    let small = `// Generated by bench/gen_corpus.js (bigfan corpus) â€” do not edit.\n\n`;
    for (let m = 0; m < BIGFAN.modulesPerSmallFile; m++) {
      small += genModule(`S${s}`, m, rng) + "\n";
    }
    fs.writeFileSync(path.join(dir, `small_${String(s).padStart(3, "0")}.ts`), small);
    loc += small.split("\n").length;
    nFiles += 1;
  }

  // entry.ts imports every file, interleaving a big every `stride` specifiers
  // so the bigs land on BFS ids 1, 1+stride, 1+2*stride, ... (all â‰ˇ 1 mod 4).
  const imports = [];
  let bi = 0;
  let si = 0;
  let pos = 0;
  while (bi < BIGFAN.bigFiles || si < BIGFAN.smallFiles) {
    if (pos % BIGFAN.stride === 0 && bi < BIGFAN.bigFiles) {
      imports.push({ from: `./big_${bi}`, name: `countFBIG${bi}M0` });
      bi += 1;
    } else if (si < BIGFAN.smallFiles) {
      imports.push({ from: `./small_${String(si).padStart(3, "0")}`, name: `countFS${si}M0` });
      si += 1;
    } else {
      imports.push({ from: `./big_${bi}`, name: `countFBIG${bi}M0` });
      bi += 1;
    }
    pos += 1;
  }
  let entry = `// Generated by bench/gen_corpus.js (bigfan corpus) â€” do not edit.\n\n`;
  for (const imp of imports) entry += `import { ${imp.name} } from "${imp.from}";\n`;
  entry += `\nexport const total: number = ${imports.map((i) => i.name).join(" + ")};\n`;
  fs.writeFileSync(path.join(dir, "entry.ts"), entry);
  loc += entry.split("\n").length;

  fs.writeFileSync(
    path.join(dir, "tsconfig.json"),
    JSON.stringify(
      {
        compilerOptions: {
          strict: true,
          noEmit: true,
          target: "esnext",
          module: "esnext",
          moduleResolution: "bundler",
          lib: ["esnext"],
          types: [],
        },
        files: ["entry.ts"],
      },
      null,
      2,
    ) + "\n",
  );
  console.log(`generated corpus 'bigfan': ${nFiles} files, ~${loc} LOC in ${dir}`);
  return loc;
}

// --- deps corpus (ROADMAP Â§5, M13: resolution cache) --------------------------
//
// Many app files in one `src/` directory, each importing a rotating subset of
// shared packages by *bare specifier* from a generated `node_modules/` tree
// (package.json + in-subset `.d.ts`). Without a resolution memo, the same
// specifier imported from K files walks `node_modules` K times; with the memo
// (keyed on `(importer_dir, spec)`), it walks once â€” the whole point of the
// corpus. Measured via `ztsc --timing`'s "resolve cache" line, comparing
// probe counts with and without `--no-resolve-cache`.
const DEPS = {
  packages: 12,
  appFiles: 60,
  importsPerFile: 5, // bare specifiers per app file
  modulesPerFile: 2, // local checking work per app file
};

function depsPkgSpec(i) {
  // Half scoped, half plain â€” both node_modules-walk shapes.
  return i % 2 === 0 ? `@acme/pkg${i}` : `libpkg${i}`;
}

function depsPkgDir(i) {
  return path.join("node_modules", depsPkgSpec(i));
}

function genDepsPackage(i) {
  // In-subset declaration surface (no conditional/mapped/template types).
  return `// Generated by bench/gen_corpus.js (deps corpus) â€” do not edit.
export interface Opts${i} {
  verbose?: boolean;
  scale: number;
}
export declare function run${i}(x: number): number;
export declare const version${i}: string;
`;
}

function genDepsAppFile(f, rng) {
  let out = `// Generated by bench/gen_corpus.js (deps corpus) â€” do not edit.\n\n`;
  const used = [];
  for (let k = 0; k < DEPS.importsPerFile; k++) {
    const i = (f + k * 5) % DEPS.packages;
    if (!used.includes(i)) used.push(i);
  }
  for (const i of used) {
    out += `import { run${i}, version${i}, Opts${i} } from "${depsPkgSpec(i)}";\n`;
  }
  out += "\n";
  for (let m = 0; m < DEPS.modulesPerFile; m++) {
    out += genModule(`APP${f}`, m, rng) + "\n";
  }
  for (const [k, i] of used.entries()) {
    out += `const opt${f}_${k}: Opts${i} = { scale: ${k} };\n`;
    out += `export const useR${f}_${k}: number = run${i}(opt${f}_${k}.scale);\n`;
    out += `export const useV${f}_${k}: string = version${i};\n`;
  }
  return out;
}

function genDeps(root, force) {
  const dir = path.join(root, "deps");
  if (fs.existsSync(dir) && !force) {
    console.log(`corpus 'deps' already exists at ${dir} (use --force to regenerate)`);
    return 0;
  }
  fs.mkdirSync(dir, { recursive: true });
  const rng = makeRng(0xdecaf);
  let loc = 0;
  let nFiles = 1; // entry.ts

  // Shared packages in node_modules.
  for (let i = 0; i < DEPS.packages; i++) {
    const pdir = path.join(dir, depsPkgDir(i));
    fs.mkdirSync(pdir, { recursive: true });
    fs.writeFileSync(
      path.join(pdir, "package.json"),
      JSON.stringify({ name: depsPkgSpec(i), version: "1.0.0", types: "index.d.ts" }, null, 2) + "\n",
    );
    const idx = genDepsPackage(i);
    fs.writeFileSync(path.join(pdir, "index.d.ts"), idx);
    loc += idx.split("\n").length;
    nFiles += 1;
  }

  // App files, all in one src/ directory (shared importer_dir -> the memo
  // collapses every repeated bare specifier to a single node_modules walk).
  const sdir = path.join(dir, "src");
  fs.mkdirSync(sdir, { recursive: true });
  for (let f = 0; f < DEPS.appFiles; f++) {
    const content = genDepsAppFile(f, rng);
    fs.writeFileSync(path.join(sdir, `app_${String(f).padStart(3, "0")}.ts`), content);
    loc += content.split("\n").length;
    nFiles += 1;
  }

  // entry.ts pulls in every app file (relative), reaching all packages.
  let entry = `// Generated by bench/gen_corpus.js (deps corpus) â€” do not edit.\n\n`;
  for (let f = 0; f < DEPS.appFiles; f++) {
    entry += `import { useR${f}_0 } from "./src/app_${String(f).padStart(3, "0")}";\n`;
  }
  entry += `\nexport const total: number = ${Array.from(
    { length: DEPS.appFiles },
    (_, f) => `useR${f}_0`,
  ).join(" + ")};\n`;
  fs.writeFileSync(path.join(dir, "entry.ts"), entry);
  loc += entry.split("\n").length;

  fs.writeFileSync(
    path.join(dir, "tsconfig.json"),
    JSON.stringify(
      {
        compilerOptions: {
          strict: true,
          noEmit: true,
          target: "esnext",
          module: "esnext",
          moduleResolution: "bundler",
          lib: ["esnext"],
          types: [],
        },
        files: ["entry.ts"],
      },
      null,
      2,
    ) + "\n",
  );
  console.log(`generated corpus 'deps': ${nFiles} files, ~${loc} LOC in ${dir}`);
  return loc;
}

function main() {
  const force = process.argv.includes("--force");
  const root = path.join(__dirname, "corpus");
  let totalLoc = 0;
  for (const [name, cfg] of Object.entries(SIZES)) {
    const dir = path.join(root, name);
    if (fs.existsSync(dir) && !force) {
      console.log(`corpus '${name}' already exists at ${dir} (use --force to regenerate)`);
      continue;
    }
    fs.mkdirSync(dir, { recursive: true });
    const rng = makeRng(0xc0ffee + Object.keys(SIZES).indexOf(name));
    let loc = 0;
    for (let f = 0; f < cfg.files; f++) {
      const content = genFile(f, cfg.modulesPerFile, rng);
      fs.writeFileSync(path.join(dir, `file_${String(f).padStart(3, "0")}.ts`), content);
      loc += content.split("\n").length;
    }
    // Minimal tsconfig so `tsc`/`tsgo` can check the corpus for comparison runs.
    fs.writeFileSync(
      path.join(dir, "tsconfig.json"),
      JSON.stringify(
        {
          compilerOptions: {
            strict: true,
            noEmit: true,
            target: "es2020",
            module: "es2020",
            lib: ["es2020"],
          },
          include: ["*.ts"],
        },
        null,
        2,
      ) + "\n",
    );
    console.log(`generated corpus '${name}': ${cfg.files} files, ~${loc} LOC in ${dir}`);
    totalLoc += loc;
  }
  totalLoc += genMulti(root, force);
  totalLoc += genSkewed(root, force);
  totalLoc += genBigfan(root, force);
  totalLoc += genDeps(root, force);
  if (totalLoc > 0) console.log(`total: ~${totalLoc} LOC`);
}

